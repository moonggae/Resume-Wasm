package com.ccc.resume.util

fun isValidUri(input: String): Boolean {
    val uriRegex = Regex(
        "^(?:(?:https?|ftp)://)" +  // 스킴
                "(?:\\S+(?::\\S*)?@)?" +  // 사용자 정보
                "(?:" +
                "(?!(?:10|127)(?:\\.\\d{1,3}){3})" +  // IP 주소 제외
                "(?!(?:169\\.254|192\\.168)(?:\\.\\d{1,3}){2})" +
                "(?!172\\.(?:1[6-9]|2\\d|3[0-1])(?:\\.\\d{1,3}){2})" +
                "(?:[1-9]\\d?|1\\d\\d|2[01]\\d|22[0-3])" +
                "(?:\\.(?:1?\\d{1,2}|2[0-4]\\d|25[0-5])){2}" +
                "(?:\\.(?:[1-9]\\d?|1\\d\\d|2[0-4]\\d|25[0-4]))" +
                "|" +
                "(?:" +
                "(?:" +
                "[a-z0-9\\u00a1-\\uffff]" +
                "[a-z0-9\\u00a1-\\uffff_-]{0,62}" +
                ")?" +
                "[a-z0-9\\u00a1-\\uffff]\\." +
                ")+" +
                "(?:[a-z\\u00a1-\\uffff]{2,}\\.?)" +
                ")" +
                "(?::\\d{2,5})?" +  // 포트
                "(?:[/?#]\\S*)?" +  // 경로, 쿼리 문자열, 프래그먼트
                "$",
        RegexOption.IGNORE_CASE
    )

    return uriRegex.matches(input)
}